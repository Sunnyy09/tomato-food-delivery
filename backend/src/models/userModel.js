import mongoose from "mongoose";

const userSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
      trim: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
      trim: true,
    },
    fullName: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
    token: {
      type: String,
    },
    cartData: {
      type: Object,
      default: {},
    },
  },
  { timestamps: true },
  { minimize: false }
);

const User = mongoose.models.user || mongoose.model("User", userSchema);
export default User;
